package projectnane.copy;

public class incdec 
{
	public static void main (String[] args)
	{
	int a=6;
	// increament
	System.out.println(a);//a=6
	System.out.println(a++);//print a=6 save a=7
	System.out.println(++a);// print a=8
	//decrement
	System.out.println(a);// print a=8
	System.out.println(a--);//print a=8 save a=7
	System.out.println(--a);// print a=6
	}

}
